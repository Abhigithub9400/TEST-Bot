<script setup>
import axios from 'axios';
import { computed, ref, nextTick } from 'vue';
import { PhoneNumberUtil } from 'google-libphonenumber';

const requestDemoSection = ref(null);
const blogHeaderImage = ref(null);
const isLoading = ref(false);
const isSuccess = ref(false);
const isError = ref(false);
const errorMessage = ref('');
const errorMessageDiv = ref(null);
const successMessage = ref('');
const successMessageDiv = ref(null);
const name = ref('');
const email = ref('');
const phoneNumber = ref('');
const countryCode = ref('+1');
const requirements = ref('');
const invalidName = ref(false);
const invalidEmail = ref(false);
const invalidPhone = ref(false);
const invalidRequirements = ref(false);
const nameErrorMessage = ref('');
const emailErrorMessage = ref('');
const phoneErrorMessage = ref('');
const requirementsErrorMessage = ref('');

const scrollToDemoForm = () => {
  if (blogHeaderImage.value) {
    const imageBottomPosition = blogHeaderImage.value.getBoundingClientRect().bottom + window.scrollY;

    window.scrollTo({
      top: imageBottomPosition,
      behavior: "smooth",
    });

    if (requestDemoSection.value) {
      requestDemoSection.value.classList.add('highlight-form');
      setTimeout(() => {
        requestDemoSection.value.classList.remove('highlight-form');
      }, 2000);
    }
  }
};

const isValid = computed(() => 
  email.value.trim() && name.value.trim() && phoneNumber.value.trim() && 
  !invalidEmail.value && !invalidName.value && !invalidPhone.value && !invalidRequirements.value
);

const submitForm = async () => {
    isError.value = false;
    errorMessage.value = '';
    
    validateFormFields();

    if (isFormValid()) {
        isLoading.value = true;

        try {
            await new Promise((resolve) => setTimeout(resolve, 2000));
            const payload = {
                Name: name.value,
                Email: email.value,
                CountryCode: countryCode.value,
                Phone: phoneNumber.value,
                Requirements: requirements.value,
            };

            const response = await axios.post('api/manage/requestdemo', payload, {
                headers: {
                    'Content-Type': 'application/json',
                },
            });

            handleResponse(response);
        } catch (error) {
            isError.value = true;
            errorMessage.value = 'An unexpected error occurred while sending your message. Please try again later.';
            console.error('Error processing request: ', error);

            await nextTick(() => {
                if (errorMessageDiv.value) {
                    errorMessageDiv.value.scrollIntoView({ behavior: "smooth", block: "center" });
                }
            });
        } finally {
            isLoading.value = false;
        }
    }
};

const isFormValid = () => {
    return (
        !invalidEmail.value && !invalidName.value && !invalidPhone.value && !invalidRequirements.value
    );
};

const validateFormFields = () => {
    validateName();
    validatePhoneNumber();
    validateRequirements();
    validateEmail();
};

const validateName = () => {
  name.value = name.value.trim();
  
  if (!name.value) {
    nameErrorMessage.value = 'Name field cannot be left blank. Please enter your name.';
    invalidName.value = true;
  } else if (name.value.length < 3) {
    nameErrorMessage.value = 'Name must be at least 3 characters long.';
    invalidName.value = true;
  } else if (!/^[a-zA-Z\s.]+$/.test(name.value)) {
    nameErrorMessage.value = 'Name can only contain letters and spaces.';
    invalidName.value = true;
  } else {
    nameErrorMessage.value = '';
    invalidName.value = false;
  }
};

const checkPhoneNumber = () => {
    if(phoneNumber.value){
        validatePhoneNumber();
    }
}

const validatePhoneNumber = () => {
    phoneNumber.value = phoneNumber.value.trim();

    if(!phoneNumber.value || phoneNumber.value === ''){
    invalidPhone.value = true
    phoneErrorMessage.value  = "Phone Number is required.";
    return;
    }

    const phoneRegex = /^[+\d]+$/;

    if (!phoneRegex.test(phoneNumber.value)) {
        invalidPhone.value = true
        phoneErrorMessage.value = 'Phone Number contains invalid characters.';
        return; 
    }

    if (phoneNumber.value.length < 5) {
        invalidPhone.value = true;
        phoneErrorMessage.value = "Phone number is too short.";
        return;
    }

    var phoneNumberWithCountryCode = countryCode.value + phoneNumber.value;
    const phoneUtil = PhoneNumberUtil.getInstance();
    const parsedNumber = phoneUtil.parse(phoneNumberWithCountryCode);

    if (!phoneUtil.isValidNumber(parsedNumber)) {
    invalidPhone.value = true
    phoneErrorMessage.value = "Please enter a valid phone number for the selected country.";
    }
    else {
        invalidPhone.value = false
        phoneErrorMessage.value = "";
    }

    return;
};

const validateEmail = () => {
  email.value = email.value.trim();
  const emailPattern = 
    /^[a-zA-Z0-9][\w.-]*[a-zA-Z0-9]@[a-zA-Z0-9]+[\w.-]*[a-zA-Z0-9]\.[a-zA-Z]{2,}$/;

  const disposableEmailProviders = [
    'yopmail.com', 'mailinator.com', 'guerrillamail.com', 
    '10minutemail.com', 'aol.com', 'example.com', 
    'a.com', 'test.com'
  ];

  const [localPart, domain] = email.value.split('@');

  if (!email.value) {
    emailErrorMessage.value = 'Email field cannot be left blank. Please enter your email.'
    invalidEmail.value = true
  } else if (localPart.length < 6 || localPart.length > 30) {
    emailErrorMessage.value = 'The username must be between 6 and 30 characters long.'
    invalidEmail.value = true
  } else if (!emailPattern.test(email.value)) {
    emailErrorMessage.value = 'Please enter a valid email address.'
    invalidEmail.value = true
  } else if (disposableEmailProviders.includes(domain.toLowerCase())) {
    emailErrorMessage.value = 'The email domain is not allowed. Please use a valid provider.'
    invalidEmail.value = true
  } else {
    emailErrorMessage.value = ''
    invalidEmail.value = false
  }
};

const validateRequirements = () => {
  requirements.value = requirements.value.trim();

  if(requirements.value && (requirements.value.length < 10 || requirements.value.length > 1000)) {
    requirementsErrorMessage.value = 'Requirements must be between 10 and 1000 characters long.';
    invalidRequirements.value = true;
  } else {
    requirementsErrorMessage.value = '';
    invalidRequirements.value = false;
  }
};

const handleResponse = async (response) => {
    if (response.status === 200 && response.data.success) {
        isSuccess.value = true;
        successMessage.value = response.data.message;
        resetFormFields();
        await nextTick(() => {
                if (successMessageDiv.value) {
                    successMessageDiv.value.scrollIntoView({ behavior: "smooth", block: "center" });
                }
            });
    } else {
        isError.value = true;
        errorMessage.value = 'An unexpected error occurred while sending your message. Please try again later.';
        console.error("Error from API: ", response.data.message)
    }
};

const resetFormFields = () => {
  email.value = '';
  name.value = '';
  phoneNumber.value = '';
  requirements.value = '';
};

const clearErrorMessages = (field) => {
  switch (field) {
    case "name": {
      if (name.value) {
        nameErrorMessage.value = '';
        invalidName.value = false;
      }
      break;
    }
    case "email": {
      if (email.value) {
        emailErrorMessage.value = '';
        invalidEmail.value = false;
      }
      break;
    }
    case "phone": {
      if (phoneNumber.value) {
        phoneErrorMessage.value = '';
        invalidPhone.value = false;
